/// NPM nos permite instalar e importar librerías dentro de nuestro proyecto
/// 1. Entramos en : https://www.npmjs.com/
/// 2. Buscamos la librería (ej: axios)
/// 3. Copiamos el comando: npm i axios
/// 4. Ejecutamos en el terminal y se instalará 
/// 5. Creará los directorios (node_modules) para utilizar la librería y un package-lock.json
/// 6. En mode_modules estarán todas las librerías instaladas
/// 7. Ya podremos utilizarlo usando un import de los modulos y utilizar su métodos